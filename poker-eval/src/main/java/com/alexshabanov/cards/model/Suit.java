package com.alexshabanov.cards.model;

/**
 * Represent a Card's suit.
 */
public enum Suit {
    /**
     * Represents spades.
     * UTF code is \u2660
     */
    SPADES,

    /**
     * Represents hearts.
     * UTF code is \u2661
     */
    HEARTS,

    /**
     * Represents diamonds.
     * UTF code is \u2662
     */
    DIAMONDS,

    /**
     * Represents clubs.
     * UTF code is \u2663
     */
    CLUBS
}
