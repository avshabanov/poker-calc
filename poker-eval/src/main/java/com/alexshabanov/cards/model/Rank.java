package com.alexshabanov.cards.model;

/**
 * Represents a card's rank.
 * The card order should be preserved, as a default card comparator relies on it.
 */
public enum Rank {
    ACE,
    TWO,
    THREE,
    FOUR,
    FIVE,
    SIX,
    SEVEN,
    EIGHT,
    NINE,
    TEN,
    JACK,
    QUEEN,
    KING
}
